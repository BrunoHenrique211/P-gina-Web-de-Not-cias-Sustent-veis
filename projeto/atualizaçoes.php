        <h2 class="headline"> Brasil e Japão discutem cooperação técnica para desenvolvimento sustentável </h2>
        <p>
            <em> O ministro da Agricultura, Carlos Fávaro, se reuniu com o embaixador do Japão no Brasil, Teiji Hayashi; com o presidente da Agência de Cooperação Internacional do Japão (Jica), Akihiko Tanaka, e com a presidente da Embrapa, Silvia Massruhá, em ato na Embrapa realizado na manhã de quarta-feira (14), informou o ministério, em nota. O principal tema da reunião foi o fortalecimento da parceria entre Brasil e Japão para o desenvolvimento sustentável da agropecuária tropical.                No encontro, foram abordadas as propostas de continuidade na cooperação técnica entre os países. “Essa cooperação técnica é, sem dúvida, muito importante. Temos a determinação de fazer uma missão governamental e empresarial no Japão para levarmos essas oportunidades de interesses bilaterais e avançar nos sistemas propostos”, disse Fávaro, na nota.
                De acordo com o ministério, o lançamento da fase de execução do “Projeto de Desenvolvimento Colaborativo da Agricultura de Precisão e Digital para o Fortalecimento do Ecossistema de Inovação e a Sustentabilidade do Agro Brasileiro” também foi tratado na reunião. Segundo a presidente da Embrapa, o projeto abrirá a oportunidade de trabalho conjunto entre agência e estatal na conversão de pastagens de baixa produtividade em terras agricultáveis. A Jica mantém parcerias com a Embrapa desde sua criação, há 50 anos, observou a pasta.
                Recentemente, o assessor especial do ministério, Carlos Ernesto Augustin, afirmou ao Broadcast Agro (sistema de notícias em tempo real do Grupo Estado) que a Jica está interessada em realizar investimento externo na recuperação de pastagens brasileiras. Ele lembrou que a parceria do Brasil com o governo japonês nos anos de 1970, por meio da agência, foi decisiva para a abertura de áreas do Cerrado. A proposta inicial do governo é que a Jica possa contribuir com a conversão de 1 milhão de hectares de pastos degradados para áreas agricultáveis com financiamento a juros baixos para os produtores rurais.
                O ministério deve promover uma missão ao Japão no próximo mês, segundo a pasta.
        </em>
